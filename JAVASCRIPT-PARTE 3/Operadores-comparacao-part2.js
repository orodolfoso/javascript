/* 

Operadores de comparação

> -> Maior
< -> Menor

>= -> Maior ou igual
<= -> Menor ou igual


*/

const firstNumber = 30
const seconddNumber = 30

console.log(firstNumber > seconddNumber)

console.log(firstNumber < seconddNumber)

console.log(firstNumber >= seconddNumber)

console.log(firstNumber <= seconddNumber)